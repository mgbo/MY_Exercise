"""
A + B — сумма; 
A - B — разность; 
A * B — произведение; 
A / B — частное; 
A ** B — возведение в степень.
Полезно помнить, что квадратный корень из числа x — это x ** 0.5, 
а корень степени n это x ** (1 / n).
"""

print (str(2+2)) # 4
print (str(2+5)) # 7


print (int('2'+'2')) # 22

